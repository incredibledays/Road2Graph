class Cross_Attention(nn.Module):
    def __init__(self, dim, heads = 8, dim_head = 64, dropout = 0., softmax=True):
        super().__init__()
        inner_dim = dim_head * heads
        self.heads = heads
        self.scale = dim ** -0.5

        self.softmax = softmax
        self.to_q = nn.Linear(dim, inner_dim, bias=False)
        self.to_k = nn.Linear(dim, inner_dim, bias=False)
        self.to_v = nn.Linear(dim, inner_dim, bias=False)

        self.to_out = nn.Sequential(
            nn.Linear(inner_dim, dim),
            nn.Dropout(dropout)
        )

    def forward(self, x, m, mask = None):

        b, n, _, h = *x.shape, self.heads
        q = self.to_q(x)
        k = self.to_k(m)
        v = self.to_v(m)

        q, k, v = map(lambda t: rearrange(t, 'b n (h d) -> b h n d', h = h), [q,k,v])

        dots = torch.einsum('bhid,bhjd->bhij', q, k) * self.scale
        mask_value = -torch.finfo(dots.dtype).max

        if mask is not None:
            mask = F.pad(mask.flatten(1), (1, 0), value = True)
            assert mask.shape[-1] == dots.shape[-1], 'mask has incorrect dimensions'
            mask = mask[:, None, :] * mask[:, :, None]
            dots.masked_fill_(~mask, mask_value)
            del mask

        if self.softmax:
            attn = dots.softmax(dim=-1)
        else:
            attn = dots
        # attn = dots
        # vis_tmp(dots)

        out = torch.einsum('bhij,bhjd->bhid', attn, v)
        out = rearrange(out, 'b h n d -> b n (h d)')
        out = self.to_out(out)
        # vis_tmp2(out)

        return out


class Attention(nn.Module):
    def __init__(self, dim, heads = 8, dim_head = 64, dropout = 0.):
        super().__init__()
        inner_dim = dim_head *  heads
        self.heads = heads
        self.scale = dim ** -0.5

        self.to_qkv = nn.Linear(dim, inner_dim * 3, bias = False)
        self.to_out = nn.Sequential(
            nn.Linear(inner_dim, dim),
            nn.Dropout(dropout)
        )

    def forward(self, x, mask = None):
        b, n, _, h = *x.shape, self.heads
        qkv = self.to_qkv(x).chunk(3, dim = -1)
        q, k, v = map(lambda t: rearrange(t, 'b n (h d) -> b h n d', h = h), qkv)

        dots = torch.einsum('bhid,bhjd->bhij', q, k) * self.scale
        mask_value = -torch.finfo(dots.dtype).max

        if mask is not None:
            mask = F.pad(mask.flatten(1), (1, 0), value = True)
            assert mask.shape[-1] == dots.shape[-1], 'mask has incorrect dimensions'
            mask = mask[:, None, :] * mask[:, :, None]
            dots.masked_fill_(~mask, mask_value)
            del mask

        attn = dots.softmax(dim=-1)


        out = torch.einsum('bhij,bhjd->bhid', attn, v)
        out = rearrange(out, 'b h n d -> b n (h d)')
        out = self.to_out(out)
        return out


class Transformer(nn.Module):
    def __init__(self, dim, depth, heads, dim_head, mlp_dim, dropout):
        super().__init__()
        self.layers = nn.ModuleList([])
        for _ in range(depth):
            self.layers.append(nn.ModuleList([
                Residual(PreNorm(dim, Attention(dim, heads = heads, dim_head = dim_head, dropout = dropout))),
                Residual(PreNorm(dim, FeedForward(dim, mlp_dim, dropout = dropout)))
            ]))

    def forward(self, x, mask = None):
        for attn, ff in self.layers:
            x = attn(x, mask = mask)
            x = ff(x)
        return x


class TransformerDecoder(nn.Module):
    def __init__(self, dim, depth, heads, dim_head, mlp_dim, dropout, softmax=True):
        super().__init__()
        self.layers = nn.ModuleList([])
        for _ in range(depth):
            self.layers.append(nn.ModuleList([
                Residual2(PreNorm2(dim, Cross_Attention(dim, heads = heads,
                                                        dim_head = dim_head, dropout = dropout,
                                                        softmax=softmax))),
                Residual(PreNorm(dim, FeedForward(dim, mlp_dim, dropout = dropout)))
            ]))

    def forward(self, x, m, mask = None):
        """target(query), memory"""
        for attn, ff in self.layers:
            x = attn(x, m, mask = mask)
            x = ff(x)
        return x


class TopoDLATransformer(nn.Module):
    def __init__(self, base_name='dla34', pretrained=True, down_ratio=2, eval_mode=False):
        super(TopoDLATransformer, self).__init__()
        assert down_ratio in [2, 4, 8, 16]
        self.eval_mode = eval_mode
        self.first_level = int(np.log2(down_ratio))
        self.base = globals()[base_name](pretrained=pretrained, return_levels=True)
        channels = self.base.channels
        scales = [2 ** i for i in range(len(channels[self.first_level:]))]
        self.dla_up = DLAUp(channels[self.first_level:], scales=scales)
        self.head = nn.Sequential(nn.ConvTranspose2d(channels[self.first_level], 32, (4, 4), (2, 2), (1, 1)), nn.ReLU(inplace=True), nn.Conv2d(32, 32, (3, 3), padding=1), nn.ReLU(inplace=True), nn.Conv2d(32, 7, (3, 3), padding=1))

        self.token_len = 4
        self.conv_t1 = nn.Conv2d(512, 32, kernel_size=3, padding=1)
        self.conv_t2 = nn.Conv2d(256, 32, kernel_size=3, padding=1)
        self.conv_t3 = nn.Conv2d(128, 32, kernel_size=3, padding=1)
        self.conv_t4 = nn.Conv2d(64, 32, kernel_size=3, padding=1)
        self.conv_t5 = nn.Conv2d(32, 32, kernel_size=3, padding=1)
        self.conv_a = nn.Conv2d(32, self.token_len, kernel_size=1, padding=0, bias=False)
        self.pos_embedding = nn.Parameter(torch.randn(1, self.token_len * 5, 32))
        self.pos_embedding_decoder = nn.Parameter(torch.randn(1, 32, 256, 256))
        self.transformer = Transformer(dim=32, depth=1, heads=8, dim_head=64, mlp_dim=64, dropout=0)
        self.transformer_decoder = TransformerDecoder(dim=32, depth=8, heads=8, dim_head=8, mlp_dim=64, dropout=0, softmax=True)

    def _forward_semantic_tokens(self, x):
        b, c, h, w = x.shape
        spatial_attention = self.conv_a(x)
        spatial_attention = spatial_attention.view([b, self.token_len, -1]).contiguous()
        spatial_attention = torch.softmax(spatial_attention, dim=-1)
        x = x.view([b, c, -1]).contiguous()
        tokens = torch.einsum('bln,bcn->blc', spatial_attention, x)
        return tokens

    def _forward_transformer(self, x):
        x += self.pos_embedding
        x = self.transformer(x)
        return x

    def _forward_transformer_decoder(self, x, m):
        b, c, h, w = x.shape
        x = x + self.pos_embedding_decoder
        x = rearrange(x, 'b c h w -> b (h w) c')
        x = self.transformer_decoder(x, m)
        x = rearrange(x, 'b (h w) c -> b c h w', h=h)
        return x

    def forward(self, x):
        x = self.base(x)
        token1 = self._forward_semantic_tokens(self.conv_t1(x[-1]))
        token2 = self._forward_semantic_tokens(self.conv_t2(x[-2]))
        token3 = self._forward_semantic_tokens(self.conv_t3(x[-3]))
        token4 = self._forward_semantic_tokens(self.conv_t4(x[-4]))
        token5 = self._forward_semantic_tokens(self.conv_t5(x[-5]))
        token = torch.cat([token1, token2, token3, token4, token5], dim=1)
        x = self.dla_up(x[self.first_level:])
        token = self._forward_transformer(token)
        x = self._forward_transformer_decoder(x, token)
        pre = self.head(x)
        pre = torch.split(pre, [1, 1, 1, 4], 1)
        return {'seg': pre[0], 'ver': pre[1], 'mid': pre[2], 'dxy': pre[3]}
